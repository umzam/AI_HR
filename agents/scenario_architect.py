def _parse_output(raw: str) -> dict:
    """从模型输出中提取 JSON，健壮处理各种格式。"""
    # 尝试提取代码块内的JSON
    match = re.search(r"```json\s*(\{.*?\})\s*```", raw, re.DOTALL)
    if not match:
        # 尝试直接找JSON对象
        match = re.search(r"\{.*\}", raw, re.DOTALL)
    if match:
        try:
            data = json.loads(match.group(1) if "```" in raw else match.group(0))
            return {
                "name":            data.get("name", ""),
                "description":     data.get("description", ""),
                "role_background": data.get("role_background", ""),
                "eval_rules":      data.get("eval_rules", ""),
            }
        except json.JSONDecodeError:
            pass

    # JSON解析失败：把原文拆段返回（降级）
    return {
        "name":            "",
        "description":     raw[:200] if raw else "",
        "role_background": "",
        "eval_rules":      "",
        "error":           "AI返回格式异常，已提取原始文本，请手动整理",
    }


